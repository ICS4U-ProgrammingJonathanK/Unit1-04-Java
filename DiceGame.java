import java.util.Scanner;
import java.util.concurrent.ThreadLocalRandom;

/**
 * The DiceGame program rolls dice then has the user
 * try and guess what it landed on tells the user if
 * their guess was too high or low and tells the user
 * how many guesses they took.
 *
 * @author  Jonathan Kene
 *
 * @version 2.0
 * @since   2022-02-23
 **/

final class DiceGame {

    /**
  * Main entry point into program.
  *
  * @throws NumberFormatException To get rid of style error
  * @throws com.myorg.NumberFormatException1 To gain access to throw
  */

   private DiceGame() {
        throw new NumberFormatException("Utility class");
    }

    /**
  * To get rid of style error.
  *
  *
  * @param args nothing passed in
  *
  * */

    public static void main(String[] args) {
        Scanner myObj = new Scanner(System.in);

        int random = ThreadLocalRandom.current().nextInt(1, 6 + 1);
        int userGuess = 0;
        int counter = 0;
        while (true) {
            counter++;
            try {
                System.out.println("Guess a number between 1 and 6");
                String userGuessString = myObj.nextLine();
                userGuess = Integer.parseInt(userGuessString);
            } catch (NumberFormatException exception) {
                counter--;
                userGuess = -1;
            }

            if (userGuess == -1) {
                System.out.println("You must type in an integer");
            } else if (userGuess == random) {
                break;
            } else if (userGuess > random) {
                System.out.println("Your number was too big");
            } else if (userGuess < random) {
                System.out.println("Your number was too small");
            } else {
                System.out.println("Please Try Again?");
            }
        }
        System.out.println("Congratulations! It took you "
                    + counter + " guesses to guess the number");
        myObj.close();
    }
}
